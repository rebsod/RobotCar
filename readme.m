## 🚗 Arduino Car Project

🌟 **Overview:** This was a collaborative group project that combined our skills in planning, coding, and engineering to bring a car to life!

- 📅 **Project Management:** We utilized *Jira* for efficient planning and task management.
- 🔧 **Assembly:** Assembled the physical parts of the car with hands-on teamwork.
- 💻 **Programming:** Wrote code in *C* to control the car's functions.
- 📡 **Remote Control:** Implemented remote controlling capabilities for ease of use.
- 🔊 **Bluetooth Integration:** Enabled Bluetooth communication for wireless control.
- 🔍 **Sensors:** Incorporated various sensors to enhance the car's functionality.

This project not only honed our technical skills but also strengthened our teamwork and problem-solving abilities! 💪
